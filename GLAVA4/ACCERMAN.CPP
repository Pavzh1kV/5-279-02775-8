/* Вычисление функции Аккермана   по Трамбле  	 \accerman.c */
#include <stdio.h>
#include <locale.h>
#include <stdlib.h>
//#include <conio.h>
static long int k;		/* для подсчета числа обращений к функции */
/* ========================================================= */
/* Функция Аккермана */
int A(int m, int n)
{
    k++;
    if (m == 0)
        return (n + 1);
    else if (n == 0)
        A(m - 1, 1);
    else
        A(m - 1, A(m, n - 1));
}

/* ====================================================== */
/* Главная функция */
/* Уже при m=4 аварийное завершение по переполнению стека */
int main()
{
    setlocale(LC_ALL,"Russian");
    int a, m, n;
    for (m = 0; m <= 4; m++)
        for (n = 0; n <= 9; n++)
        {
            k = 0;		/* для повторных обращений к функции */
            a = A(m, n);
            printf(" \n m=%d   n=%d   A=%d  k=%ld", m, n, a, k);
            getchar();
        }
    getchar();			/* ждем нажатия клавиши */
    return 0;
}

