/* Расстановка ферзей,любое число решений от 1 до 92,
		 \ferzir2.c  */
#include <stdio.h>
#include <locale.h>
#include <stdlib.h>
//#include <conio.h>
#define n 8
int a[8], i, j, x[8], b[15], c[15];
/* =============================================== */
/* ПЕЧАТЬ РЕЗУЛЬТАТОВ ОДНОГО РЕШЕНИЯ      */
void print1()
{
    int k;
    printf("\n");
    for (k = 0; k < 8; k++)
        printf(" %d ", x[k]);
}

/* ==================================================== */
/* ЗАДАННОЕ ЧИСЛО r  РЕШЕНИЙ РАССТАНОВКИ 8 ФЕРЗЕЙ       */
int Try(int i, int r)
{
    /* i - число попыток, при вызове задается 1,
       r - число решений, 1<=r<=92, при неверном
       задании r получите 92 решения */
    int j;
    int static v = 0;
    for (j = 1; j <= 8; j++)
    {
        if (a[j - 1] && b[i + j - 2] && c[i - j + 7])
        {
            x[i - 1] = j;
            a[j - 1] = 0;
            b[i + j - 2] = 0;
            c[i - j + 7] = 0;
            if (i < 8)
                Try(i + 1, r);
            else
            {
                print1();
                if (++v == r)
                    return 0;
            }
            a[j - 1] = 1;
            b[i + j - 2] = 1;
            c[i - j + 7] = 1;
        }
    }
}

/* ====================================================== */
int main()
{
    setlocale(LC_ALL,"Russian");
    int k;
    printf("\n\n Расстановка 8 ферзей");
    for (k = 0; k < n; k++)
        a[k] = 1;
    for (k = 0; k < 16; k++)
    {
        b[k] = 1;
        c[k] = 1;
    }
    Try(1, 12);
    printf("\n");
    getchar();
}

