/* Вычисление функции Аккермана по Ахо              */
/*		\algoritm\accerAxo.c */
#include <stdio.h>
#include <locale.h>
#include <stdlib.h>
//#include <conio.h>
static long int k;		/* для подсчета числа обращений к функции */
/* ============================================= */
/* Функция Аккермана по Ахо */
long int A(int x, int y)
{
    k++;
    if (x == 0)
        return 1;
    if ((x == 1) && (y == 0))
        return 2;
    if ((x >= 2) && (y == 0))
        return x + 2;
    A(A(x - 1, y), y - 1);
}

/* ============================================== */
/* Главная функция */
/* Уже при m=3 n=4 аварийное завершение по переполнению стека */
int main()
{
    setlocale(LC_ALL,"Russian");
    int m, n;
    long int a;
    for (m = 0; m <= 3; m++)
        for (n = 0; n <= 19; n++)
        {
            k = 0;		/* для повторных обращений к функции */
            a = A(4, n);
            printf(" \n m=%d   n=%d   A=%ld  k=%ld", m, n, a, k);
            getchar();
        }
    getchar();			/* ждем нажатия клавиши */
}

