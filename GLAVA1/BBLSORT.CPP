/*  ФУНКЦИЯ СОРТИРОВКИ МЕТОДОМ ПУЗЫРЬКА  */
void BblSort(int A[], int nn)
/* A - сортируемый массив целых чисел, nn - число элементов*/
{
    int i, j, k, p;
    for (i = 0; i < nn - 1; i++)
    {
        p = 0;			/* признак отсутствия перестановок */
        for (j = nn - 1; j > i; j--)
            if (A[j] < A[j - 1])
            {
                k = A[j];
                A[j] = A[j - 1];
                A[j - 1] = k;
                p = 1;
            }
        /* Если перестановок не было, то сортировка выполнена */
        if (p == 0)
            return;
        /* Отладочная печать по шагам сортировки */
        printf(" \ni = %d", i);
        for (j = 0; j < nn; j++)
            printf("\t %d", A[j]);
    }
}

