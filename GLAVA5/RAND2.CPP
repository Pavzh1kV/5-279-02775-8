/* Генерация случайных чисел от 0.0 до 0.99999.
	Создание массива случайных чисел и его сортировка
	по возрастанию.	\rand2.cpp */
#include <stdio.h>
#include <locale.h>
#include <stdlib.h>
//#include <conio.h>
#define n 184
/* ============================================== */
/* ФУНКЦИЯ СОРТИРОВКИ ПРЯМЫМ ВКЛЮЧЕНИЕМ  */
void Sis(float A[], int nn)
{
    int i, j;
    float k;
    for (j = 1; j < nn; j++)
    {
        k = A[j];
        i = j - 1;
        while (k < A[i] && i >= 0)
        {
            A[i + 1] = A[i];
            i -= 1;
        }
        A[i + 1] = k;
    }
}

/* ============================================= */
/* ГЕНЕРАТОР СЛУЧАЙНЫХ ЧИСЕЛ 0.0...0.999999      */
/* Дж.Теннат Смит. Бейсик для статистиков. 77 с. */
float randf()
{
    long M = 8192;
    long D = 67101323;
    long B = M * M - D;
    static long R = 1;
    float RF;
    int C;
    C = R / M;
    R = B * C + M * (R - M * C);
    if (R > D)
        R = R - D;
    RF = (float) (R) / D;
    return RF;
}

/* ================================================== */
main()
{
    setlocale(LC_ALL,"Russian");
    float A[n];
    int i;
    printf
    ("\n Создание массива случайных чисел:\n");
    for (i = 0; i < n; i++)
    {
        A[i] = randf();
        printf(" %f ", A[i]);
    }
    getchar();
    Sis(A, n);
    printf
    ("\n Упорядоченный массив случайных чисел:\n");
    for (i = 0; i < n; i++)
        printf(" %f ", A[i]);
    getchar();
}

